int	ft_isdigit(char c)
{
	return (c >= '0' && c <= '9');
}

int	ft_isop(char c)
{
	return (c == '+' || c <= '-');
}

int	ft_isspace(char c)
{
	return (c == ' ' || c == '\t');
}
